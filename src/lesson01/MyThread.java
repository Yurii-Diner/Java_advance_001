// Объявление пакета, к которому принадлежит класс
package lesson01;

// Создание класса MyThread, который наследуется от класса Thread
public class MyThread extends Thread {
    // Приватное поле для хранения имени потока
    private String threadName;

    // Конструктор класса, принимающий имя потока в качестве параметра
    public MyThread(String threadName) {

        // Присваивание переданного имени полю класса
        this.threadName = threadName;

        // Установка имени потока через метод setName() родительского класса Thread
        setName(threadName);        // Устанавливаем имя потока для отладки
    }

    // Переопределение метода run(), который будет выполняться при запуске потока
    @Override
    public void run() {
        // Вывод сообщения о начале работы потока
        System.out.println(threadName + " начал работу");

        // Цикл for, который выполнится 5 раз (от 0 до 4 включительно)
        for (int i = 0; i < 5; i++) {
            // Вывод текущего значения счетчика для данного потока
            System.out.println(threadName + ": счетчик = " + i);
            try {
                // Thread.sleep() - приостанавливает выполнение потока
                // Параметр устанавливается в миллисекундах
                // Приостанавливает выполнение потока на 500 миллисекунд (0.5 секунды)
                Thread.sleep(500);
            } catch (InterruptedException e) {
                // Обработка исключения, если поток был прерван во время сна
                System.out.println(threadName + " был прерван");
                // Вывод стека вызовов для отладки исключения
                e.printStackTrace();
            }
        }
        // Вывод сообщения о завершении работы потока
        System.out.println(threadName + " завершил работу");
    }
}